    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow=accountDetailsJtable.getSelectedRow();
        if(selectedRow >=0){
            int dialogueButton=JOptionPane.YES_NO_OPTION;
            int dialogueResult=JOptionPane.showConfirmDialog(null,"Would you like to delete this account?", "Warning", dialogueButton);
            if(dialogueResult==JOptionPane.YES_OPTION){
                Account account= (Account) accountDetailsJtable.getValueAt(selectedRow, 0);
                accountDirectory.deleteAccount(account);
                JOptionPane.showMessageDialog(null, "Account has been deleted");
                Populatetable();
            }
        }else{
            JOptionPane.showMessageDialog(null,"Please select a row from table first", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(!txtAccountNoSearch.getText().isEmpty()){
        String accountNumber=txtAccountNoSearch.getText();
        Account result=accountDirectory.searchAccount(accountNumber);
        if(result==null){
            JOptionPane.showMessageDialog(null,"Account number you entered does not exist", "Information", JOptionPane.INFORMATION_MESSAGE);
        }else{
            
            ViewAccountJPanel panel=new ViewAccountJPanel(userProcessContainer,result);
            userProcessContainer.add("ViewAccountJPanel",panel);
            CardLayout layout=(CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        }else{
            JOptionPane.showMessageDialog(null,"Please enter an Account number!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchActionPerformed


