    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to continue");
            return;
        }

        VaccineRequest request = (VaccineRequest) workRequestJTable.getValueAt(selectedRow, 0);
        if (request.getStatus().equalsIgnoreCase("CDCPending")) {
            JOptionPane.showMessageDialog(null, "Please asssign before Processing");
            return;
        }

        request.setStatus("Processing in Second level");

        ProcessOrderFromCdcJPanel processOrderFromCdcJPanel = new ProcessOrderFromCdcJPanel(userProcessContainer, request, system, userAccount);
        userProcessContainer.add("processWorkRequestJPanel", processOrderFromCdcJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_processJButtonActionPerformed

