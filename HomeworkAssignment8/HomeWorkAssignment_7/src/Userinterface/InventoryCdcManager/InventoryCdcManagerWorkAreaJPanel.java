    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();

        model.setRowCount(0);

        for (WorkRequest request : inventoryCdcManager.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = ((VaccineRequest) request).getSenderSecondLevel().getEmployee().getName();
            row[2] = ((VaccineRequest) request).getReceiverSecondLevel() == null ? null : ((VaccineRequest) request).getReceiverSecondLevel().getEmployee().getName();
            row[3] = request.getStatus();

            model.addRow(row);
        }
    }
