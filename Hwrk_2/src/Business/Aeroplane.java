public class Aeroplane {
    private String serialNumber;
    private String yearofManufacture;
    private String noofseats;
    private String modelno;
    private String dateofExpiry;
    private String airlinename;
    private String maintanenceDate;
    private String manufacturerName;
    private String availableStatus;
    private String maintanenceStatus;
    private String airportName;

    public String getSerialNumber() {
        return serialNumber;
    }

    public void setSerialNumber(String serialNumber) {
        this.serialNumber = serialNumber;
    }

    public String getYearofManufacture() {
        return yearofManufacture;
    }

    public void setYearofManufacture(String yearofManufacture) {
        this.yearofManufacture = yearofManufacture;
    }

    public String getNoofseats() {
        return noofseats;
    }

    public void setNoofseats(String noofseats) {
        this.noofseats = noofseats;
    }

    public String getModelno() {
        return modelno;
    }

    public void setModelno(String modelno) {
        this.modelno = modelno;
    }

    public String getDateofExpiry() {
        return dateofExpiry;
    }

    public void setDateofExpiry(String dateofExpiry) {
        this.dateofExpiry = dateofExpiry;
    }

    public String getAirlinename() {
        return airlinename;
    }

    public void setAirlinename(String airlinename) {
        this.airlinename = airlinename;
    }

    public String getMaintanenceDate() {
        return maintanenceDate;
    }

    public void setMaintanenceDate(String maintanenceDate) {
        this.maintanenceDate = maintanenceDate;
    }

    public String getManufacturerName() {
        return manufacturerName;
    }

    public void setManufacturerName(String manufacturerName) {
        this.manufacturerName = manufacturerName;
    }

    public String getAvailableStatus() {
        return availableStatus;
    }

    public void setAvailableStatus(String availableStatus) {
        this.availableStatus = availableStatus;
    }

    public String getMaintanenceStatus() {
        return maintanenceStatus;
    }

    public void setMaintanenceStatus(String maintanenceStatus) {
        this.maintanenceStatus = maintanenceStatus;
    }

    public String getAirportName() {
        return airportName;
    }

    public void setAirportName(String airportName) {
        this.airportName = airportName;
    }
    
    
}
